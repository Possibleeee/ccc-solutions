# compare expression give us true and false
print(100>50)
print(not 100>50)

print(100>=50)
# same as
print(100>50 or 100==50)

# and
print(5>2 and 100<200) # true or false
print(5>2 and 100>200) # true or false

# <, <=, !=
print(5!=5)

# and
print(True and False)
print(False and True)
print(True and True)
print(False and False)

# or
print(True or False)
print(False or True)
print(True or True)
print(False or False)